nilai = float(input("Brapa nilainya ?"))

if nilai >= 90 :
	print("Selamat, Anda mendapat nilai A")
elif nilai >= 80 :
	print("Lumayan, Anda mendapat nilai B")
elif nilai >= 70 :
	print("Cukup, Anda mendapat nilai C")
elif nilai >= 60 :
	print("Biasa, Anda mendapat nilai D")
else :
	print("Kurang, Berusahalah lagi, Anda mendapat nilai F")
	